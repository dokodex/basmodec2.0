      <h1>Como prepara una habitación antes de pintar</h1>
      <div class="noticias__text">
        <p>
          <h2>1. Proteger el espacio para evitar manchar. Para eso lo que
          necesitamos es:</h2>
          · Cinta de carrocero para proteger interruptores, enchufes,
          zócalos…<br>
          · Plástico para cubrir muebles y cosas que queramos
          proteger.<br>
          · Papel o papel con cinta para cubrir el suelo u otras
          zonas.
        </p>
        <p>
          <h2>2. Prepara la superficie a pintar.</h2> Rellenaremos las grietas que
          pueda haber en las paredes o techos con masilla extendiéndola
          con una espátula.<br><br>
          Debemos dejar secar la masilla y pasar una lija para que quede la
          superficie quede lisa y lista para pintar. Después pasamos un
          paño para quitar el polvo que haya quedado y ya podemos
          comenzar a pintar!
        </p>
